package chapterTwo;

public class TwoPointFourteen {
    public static void main(String[] args) {
        int a,b,c,d;
        a = 1; b = 2; c = 3; d = 4;
        System.out.println(a+" "+b+" "+c+" "+d);
        System.out.print(a + " ");
        System.out.print(b + " ");
        System.out.print(c + " ");
        System.out.print(d);
        System.out.printf("%n%d %d %d %d", a,b,c,d);
    }
}
